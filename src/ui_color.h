inline raylib::Color getOppositeColor(const raylib::Color& color) {
    return raylib::Color{                                            //
                         static_cast<unsigned char>(255 - color.r),  //
                         static_cast<unsigned char>(255 - color.g),  //
                         static_cast<unsigned char>(255 - color.b),  //
                         color.a};
}

inline unsigned char comp_min(const raylib::Color& a) {
    return (unsigned char) std::min({a.r, a.g, a.b});
}

inline unsigned char comp_max(const raylib::Color& a) {
    return (unsigned char) std::max({a.r, a.g, a.b});
}

raylib::vec3 toHSL(const raylib::Color color) {
    const auto cmax = comp_max(color);
    const auto cmin = comp_min(color);
    const auto delta = cmax - cmin;

    raylib::vec3 hsl = {0.f, 0.f, (cmax + cmin) / 2.f};

    if (abs(delta) <= EPSILON) {
        return hsl;
    }

    if (abs(cmax - color.r) <= EPSILON) {
        hsl.x = static_cast<float>(std::fmod((color.g - color.b) / delta, 6.f));
    } else if (abs(cmax - color.g) <= EPSILON) {
        hsl.x = (color.b - color.r) / delta + 2.f;
    } else {
        hsl.x = (color.r - color.g) / delta + 4.f;
    }

    hsl.y = delta / (1.f - std::abs(2.f * hsl.z - 1.f));
    hsl.x /= 6.f;
    hsl.x = std::fmod(hsl.x + 1.f, 1.f);
    return hsl;
}

raylib::Color toRGB(const raylib::vec3& hsl) {
    const float k = hsl.x * 6.f;
    const float c = (1.f - std::abs(2.f * hsl.z - 1.f)) * hsl.y;
    const float x = c * (1.f - std::abs(std::fmod(k, 2.f) - 1.f));
    const float m = hsl.z - c / 2.f;
    const int d = int(std::floor(k));

    raylib::vec3 rgb;

    switch (d) {
        case 0:
            rgb = {c, x, 0.f};
            break;
        case 1:
            rgb = {x, c, 0.f};
            break;
        case 2:
            rgb = {0.f, c, x};
            break;
        case 3:
            rgb = {0.f, x, c};
            break;
        case 4:
            rgb = {x, 0.f, c};
            break;
        default:
            rgb = {c, 0.f, x};
            break;
    }

    return raylib::Color{
        (unsigned char) (255 * (rgb.x + m)),
        (unsigned char) (255 * (rgb.y + m)),
        (unsigned char) (255 * (rgb.z + m)),
        (unsigned char) 255,
    };
}

inline raylib::Color getHighlighted(const raylib::Color& color) {
    auto hsl = toHSL(color);
    hsl.z = (hsl.z + 0.01f);
    return toRGB(hsl);
}

